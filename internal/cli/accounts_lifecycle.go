func newAccountsReactivateCmd() *cobra.Command {
	var (
		unlimited  bool
		expiration string
		yes        bool
	)
	cmd := &cobra.Command{
		Use:   "reactivate <account-id>",
		Short: "Reactivate an expired account",
		Long: `Reactivate an expired account. Specify exactly one of --unlimited (no
expiration) or --expiration (an RFC3339 timestamp) to set the new license
window.`,
		Args: cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			id := args[0]
			if err := validateReactivateChoice(unlimited, expiration); err != nil {
				return err
			}
			return guard(cmd.OutOrStdout(), "accounts reactivate", "reactivate account "+id, id, yes, func() error {
				c, err := mgmtClient()
				if err != nil {
					return err
				}
				if err := c.AccountsReactivate(cmd.Context(), id, unlimited, expiration); err != nil {
					return err
				}
				if outputFormat == "json" {
					return printJSON(cmd.OutOrStdout(), map[string]string{"status": "reactivated", "id": id})
				}
				fmt.Fprintf(cmd.OutOrStdout(), "Reactivated account %s\n", id)
				return nil
			})
		},
	}
	cmd.Flags().BoolVar(&unlimited, "unlimited", false, "reactivate with no expiration")
	cmd.Flags().StringVar(&expiration, "expiration", "", "new expiration as an RFC3339 timestamp")
	cmd.Flags().BoolVar(&yes, "yes", false, "apply the action (default: dry-run)")
	return markJSON(cmd)
}


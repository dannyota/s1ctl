func newUpdatesDeployCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "deploy",
		Short: "Manage Sentinel Deploy credential groups",
		Long: `Manage credential groups used by Sentinel Deploy (Ranger auto-deploy)
to install agents on unprotected endpoints.

Credential groups contain encrypted credentials that agents use to
authenticate when deploying to new endpoints.`,
	}
	requireSubcommand(cmd)
	cmd.AddCommand(newDeployCredGroupListCmd())
	cmd.AddCommand(newDeployCredGroupCreateCmd())
	cmd.AddCommand(newDeployCredGroupDeleteCmd())
	cmd.AddCommand(newDeployCredDetailListCmd())
	cmd.AddCommand(newDeployCredDetailAddCmd())
	cmd.AddCommand(newDeployCredDetailUpdateCmd())
	cmd.AddCommand(newDeployCredDetailDeleteCmd())
	return cmd
}

func newDeployCredGroupListCmd() *cobra.Command {
	var (
		siteIDs    []string
		accountIDs []string
		groupName  string
		targetOS   string
		cursor     string
		sortBy     string
		sortOrder  string
		limit      int
		all        bool
	)

	cmd := &cobra.Command{
		Use:   "list-groups",
		Short: "List credential groups",
		RunE: func(cmd *cobra.Command, _ []string) error {
			c, err := mgmtClient()
			if err != nil {
				return err
			}
			params := &mgmt.DeployCredGroupListParams{
				SiteIDs:       siteIDs,
				AccountIDs:    accountIDs,
				GroupNameLike: groupName,
				TargetOS:      targetOS,
				Cursor:        cursor,
				SortBy:        sortBy,
				SortOrder:     sortOrder,
				Limit:         limit,
			}
			if params.Limit == 0 {
				params.Limit = defaultPageSize
			}

			var items []mgmt.DeployCredGroup
			var total int

			if all {
				items, total, err = fetchAllREST("cred group", func(cur string) ([]mgmt.DeployCredGroup, *mgmt.Pagination, error) {
					params.Cursor = cur
					return c.DeployCredGroupList(cmd.Context(), params)
				})
			} else {
				var pag *mgmt.Pagination
				items, pag, err = c.DeployCredGroupList(cmd.Context(), params)
				if pag != nil {
					total = pag.TotalItems
				}
			}
			if err != nil {
				return err
			}

			headers := []string{"ID", "Name", "Domain", "OS", "Scope ID", "Details"}
			rows := make([][]string, len(items))
			for i, g := range items {
				rows[i] = []string{
					g.ID, g.GroupName, orDash(g.Domain),
					string(g.TargetOS), g.ScopeID,
					fmt.Sprintf("%d", g.TotalDetails),
				}
			}
			return printOutput(cmd.OutOrStdout(), headers, rows, items, len(items), total, "cred group", all)
		},
	}
	cmd.Flags().StringSliceVar(&siteIDs, "site-id", nil, "filter by site ID")
	cmd.Flags().StringSliceVar(&accountIDs, "account-id", nil, "filter by account ID")
	cmd.Flags().StringVar(&groupName, "group-name", "", "filter by group name (substring)")
	cmd.Flags().StringVar(&targetOS, "target-os", "", "filter by target OS (windows|osx_linux)")
	cmd.Flags().IntVar(&limit, "limit", 0, "max results per page (default 50)")
	cmd.Flags().BoolVar(&all, "all", false, "fetch all pages")
	cmd.Flags().StringVar(&cursor, "cursor", "", "pagination cursor")
	cmd.Flags().StringVar(&sortBy, "sort-by", "", "sort field (groupName|createdAt|updatedAt|domain|targetOs)")
	cmd.Flags().StringVar(&sortOrder, "sort-order", "", "sort direction (asc|desc)")
	return markJSON(cmd)
}

func newDeployCredGroupCreateCmd() *cobra.Command {
	var (
		groupName       string
		groupPassphrase string
		scopeID         string
		domain          string
		targetOS        string
		yes             bool
	)

	cmd := &cobra.Command{
		Use:   "create-group",
		Short: "Create a credential group",
		Long: `Create a new Sentinel Deploy credential group.

The passphrase is an encrypted value generated by the agent; the
management console does not know the decryption key.`,
		RunE: func(cmd *cobra.Command, _ []string) error {
			if groupName == "" {
				return fmt.Errorf("--group-name is required")
			}
			if groupPassphrase == "" {
				return fmt.Errorf("--group-passphrase is required")
			}
			if scopeID == "" {
				return fmt.Errorf("--scope-id is required")
			}

			input := mgmt.DeployCredGroupCreateInput{
				GroupName:       groupName,
				GroupPassphrase: groupPassphrase,
				ScopeID:         scopeID,
			}
			if domain != "" {
				input.Domain = &domain
			}
			if targetOS != "" {
				os := mgmt.DeployTargetOS(targetOS)
				input.TargetOS = &os
			}

			action := fmt.Sprintf("create cred group %q (scope=%s)", groupName, scopeID)
			return guard(cmd.OutOrStdout(), "updates deploy create-group", action, groupName, yes, func() error {
				c, err := mgmtClient()
				if err != nil {
					return err
				}
				g, err := c.DeployCredGroupCreate(cmd.Context(), input)
				if err != nil {
					return err
				}
				if outputFormat == "json" {
					return printJSON(cmd.OutOrStdout(), g)
				}
				fmt.Fprintf(cmd.OutOrStdout(), "Created cred group %s (%s)\n", g.ID, g.GroupName)
				return nil
			})
		},
	}
	cmd.Flags().StringVar(&groupName, "group-name", "", "credential group name")
	cmd.Flags().StringVar(&groupPassphrase, "group-passphrase", "", "encrypted passphrase")
	cmd.Flags().StringVar(&scopeID, "scope-id", "", "scope ID (site ID)")
	cmd.Flags().StringVar(&domain, "domain", "", "domain associated with the group")
	cmd.Flags().StringVar(&targetOS, "target-os", "", "target OS (windows|osx_linux)")
	cmd.Flags().BoolVar(&yes, "yes", false, "apply the action (default: dry-run)")
	return markJSON(cmd)
}


func newDeployCredDetailAddCmd() *cobra.Command {
	var (
		credGroupID   string
		title         string
		credType      string
		encryptedKey  string
		encryptedCred string
		yes           bool
	)

	cmd := &cobra.Command{
		Use:   "add-detail",
		Short: "Add a credential detail to a group",
		RunE: func(cmd *cobra.Command, _ []string) error {
			if credGroupID == "" {
				return fmt.Errorf("--cred-group-id is required")
			}
			if title == "" {
				return fmt.Errorf("--title is required")
			}
			if credType == "" {
				return fmt.Errorf("--cred-type is required")
			}
			if encryptedKey == "" {
				return fmt.Errorf("--encrypted-key is required")
			}
			if encryptedCred == "" {
				return fmt.Errorf("--encrypted-cred is required")
			}

			action := fmt.Sprintf("add cred detail %q to group %s", title, credGroupID)
			return guard(cmd.OutOrStdout(), "updates deploy add-detail", action, title, yes, func() error {
				c, err := mgmtClient()
				if err != nil {
					return err
				}
				err = c.DeployCredDetailAdd(cmd.Context(), mgmt.DeployCredDetailAddInput{
					CredGroupID: credGroupID,
					Details: []mgmt.DeployCredDetailInput{
						{
							Title:         title,
							CredType:      credType,
							EncryptedKey:  encryptedKey,
							EncryptedCred: encryptedCred,
						},
					},
				})
				if err != nil {
					return err
				}
				if outputFormat == "json" {
					return printJSON(cmd.OutOrStdout(), map[string]string{"status": "added", "credGroupId": credGroupID})
				}
				fmt.Fprintf(cmd.OutOrStdout(), "Added cred detail %q to group %s\n", title, credGroupID)
				return nil
			})
		},
	}
	cmd.Flags().StringVar(&credGroupID, "cred-group-id", "", "credential group ID")
	cmd.Flags().StringVar(&title, "title", "", "credential title")
	cmd.Flags().StringVar(&credType, "cred-type", "", "credential type (e.g. User/Password)")
	cmd.Flags().StringVar(&encryptedKey, "encrypted-key", "", "encrypted key")
	cmd.Flags().StringVar(&encryptedCred, "encrypted-cred", "", "encrypted credential")
	cmd.Flags().BoolVar(&yes, "yes", false, "apply the action (default: dry-run)")
	return markJSON(cmd)
}

